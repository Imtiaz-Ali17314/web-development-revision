// Assignment Operators
// = , += , -= , *= , %= , **=

let a = 12;
let b = 5;
let c = 8;
let d = 13;
let e = 2;

// a = a+2    Addition
console.log("a = ", (a += 3));

// b = b-2    Subtraction
console.log("b = ", (b -= 2));

// c = c*2    Multiplication
console.log("c = ", (c *= 2));

// d = d%2    module
console.log("d = ", (d %= 2));

// e = e**2   Power
console.log("e = ", (e **= 2));
